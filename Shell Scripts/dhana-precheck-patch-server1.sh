#!/bin/bash
host=/home/dhanapal.ikt/hostipaddress #( hostip address in file)
path=/home/dhanapal.ikt/precheck_patch

for ip in "$(cat $host)"
do
 echo "$ip is remotely login"
  sshpass -p root123 ssh $ip " echo '=======Precheck_outputs========' >> $path ; 
  df -Th >> $path ;
  echo '=============================================================================================== ' >> $path ;
  sudo lvs >> $path ;
  echo '=============================================================================================== ' >> $path ;
  sudo vgs >> $path ;
  echo '=============================================================================================== ' >> $path ;

  pvs >> $path ;
  echo '=============================================================================================== ' >> $path ;
  cat /etc/fstab >> $path;
  echo '=============================================================================================== ' >> $path ;
  cat /etc/resolv.conf >> $path ;
  echo '=============================================================================================== ' >> $path ;
  cat /etc/hosts >> $path ;
  echo '=============================================================================================== ' >> $path ;
  route -n >> $path ;
  echo '=============================================================================================== ' >> $path ;
  netstat -rn >> $path ;
  echo '=============================================================================================== ' >> $path ;
  ifconfig -a >> $path ;
  echo '=============================================================================================== ' >> $path ;
  cat /etc/os-release >> $path ;
  echo '=============================================================================================== ' >> $path ;
  hostnamectl >> $path ;
  echo '=============================================================================================== ' >> $path ;
  uname -a >> $path ;
  echo '=============================================================================================== ' >> $path ;
  exit"
 exit
done

